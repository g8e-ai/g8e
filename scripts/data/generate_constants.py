def load_json(filename: str) -> dict[str, Any]:
    """Load a JSON file from protocol/constants/."""
    path = PROTOCOL_CONSTANTS_DIR / filename
    if not path.exists():
        print(f"Error: {path} not found", file=sys.stderr)
        sys.exit(1)
    with open(path) as f:
        return json.load(f)


