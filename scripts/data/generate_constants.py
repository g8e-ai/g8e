def generate_go_status() -> str:
    """Generate Go constants from status.json."""
    data = load_json("status.json")
    lines = [go_header("status.json", "constants"), "\n"]
    
    # Filter out metadata and comments
    sections = {k: v for k, v in data.items() if not k.startswith("_")}
    
    # Track assigned constant names to detect collisions
    assigned_go_consts: dict[str, str] = {} # const_name -> original_key
    
    # 1. Generate types and constants for each section
    for key, values in sections.items():
        # Get type name from _meta or derive from key
        meta = values.get("_meta", {})
        type_name = meta.get("_go_type")
        if type_name is None:
            print(f"Error: Status section '{key}' missing required '_go_type' field in _meta", file=sys.stderr)
            sys.exit(1)
        
        lines.append(f"// {type_name} is a typed string for {key.replace('.', ' ')}.\n")
        # Use defined type for stricter type safety
        lines.append(f"type {type_name} string\n\n")
        lines.append("const (\n")
        for val_key, entry in values.items():
            if val_key.startswith("_"):
                continue
                
            if not isinstance(entry, dict) or "value" not in entry:
                print(f"Error: Status entry '{key}.{val_key}' must be an object with 'value' field", file=sys.stderr)
                sys.exit(1)
            
            value = entry["value"]
            const_name = entry.get("_go_const")
            if const_name is None:
                print(f"Error: Status entry '{key}.{val_key}' missing required '_go_const' field", file=sys.stderr)
                sys.exit(1)
            
            # Uniqueness check
            if const_name in assigned_go_consts:
                print(f"Error: Constant name collision in status.go: '{const_name}' generated from both '{assigned_go_consts[const_name]}' and '{key}.{val_key}'", file=sys.stderr)
                sys.exit(1)
            assigned_go_consts[const_name] = f"{key}.{val_key}"
            
            # Use the typed alias for documentation, though it's still just a string
            lines.append(f'\t{const_name} {type_name} = "{value}"\n')
        lines.append(")\n\n")

    # 2. Generate the global Status variable with typed fields for type safety
    lines.append("// Status groups all status constants for easy access.\n")
    lines.append("var Status = struct {\n")
    for key, values in sections.items():
        meta = values.get("_meta", {})
        type_name = meta.get("_go_type")
        if type_name is None:
            print(f"Error: Status section '{key}' missing required '_go_type' field in _meta", file=sys.stderr)
            sys.exit(1)
            
        field_name = meta.get("_go_name")
        if field_name is None:
            print(f"Error: Status section '{key}' missing required '_go_name' field in _meta", file=sys.stderr)
            sys.exit(1)

        lines.append(f"\t{field_name} struct {{\n")
        for val_key, entry in values.items():
            if val_key.startswith("_"):
                continue
            field_name_val = entry.get("_go_name")
            if field_name_val is None:
                print(f"Error: Status entry '{key}.{val_key}' missing required '_go_name' field", file=sys.stderr)
                sys.exit(1)
            lines.append(f"\t\t{field_name_val} {type_name}\n")
        lines.append("\t}\n")
    
    lines.append("}{\n")
    
    for key, values in sections.items():
        meta = values.get("_meta", {})
        type_name = meta.get("_go_type")
        if type_name is None:
            print(f"Error: Status section '{key}' missing required '_go_type' field in _meta", file=sys.stderr)
            sys.exit(1)
            
        field_name = meta.get("_go_name")
        lines.append(f"\t{field_name}: struct {{\n")
        for val_key, entry in values.items():
            if val_key.startswith("_"):
                continue
            field_name_val = entry.get("_go_name")
            lines.append(f"\t\t{field_name_val} {type_name}\n")
        lines.append("\t}{\n")
        for val_key, entry in values.items():
            if val_key.startswith("_"):
                continue
            field_name_val = entry.get("_go_name")
            const_name = entry.get("_go_const")
            if const_name is None:
                print(f"Error: Status entry '{key}.{val_key}' missing required '_go_const' field", file=sys.stderr)
                sys.exit(1)
            lines.append(f"\t\t{field_name_val}: {const_name},\n")
        lines.append("\t},\n")
    
    lines.append("}\n")
    
    return "".join(lines)


def generate_go_env_vars() -> str:
    """Generate Go constants from env_vars.json."""
    data = load_json("env_vars.json")
    lines = [go_header("env_vars.json", "constants"), "\n"]
    
    lines.append("// EnvVarKey is a typed string for environment variable names.\n")
    lines.append("type EnvVarKey string\n\n")
    lines.append("// EnvVar groups all environment variable name constants consumed by g8eo.\n")
    lines.append("var EnvVar = struct {\n")
    
    all_vars = [] # List of (const_name, env_key)
    assigned_consts = {} # const_name -> key
    
    for key, entry in data.items():
        if key.startswith("_"):
            continue
            
        if isinstance(entry, dict):
            value = entry.get("value", key)
            const_name = entry.get("_go_const")
        else:
            value = entry # Fallback for old flat format if any
            const_name = None

            const_name = entry.get("_go_const")
            if const_name is None:
                print(f"Error: Env var entry '{key}' missing required '_go_const' field", file=sys.stderr)
                sys.exit(1)
            
        if const_name in assigned_consts:
            print(f"Error: Constant name collision in env_vars.go: '{const_name}' generated from both '{assigned_consts[const_name]}' and '{key}'", file=sys.stderr)
            sys.exit(1)
        assigned_consts[const_name] = key
        
        all_vars.append((const_name, value))
        lines.append(f'\t{const_name} EnvVarKey\n')
    
    lines.append("}{\n")
    
    # Initialize values
    for const_name, env_key in all_vars:
        lines.append(f'\t{const_name}: "{env_key}",\n')
    
    lines.append("}\n")
    return "".join(lines)


