def go_header(filename: str, package: str, imports: list[str] | None = None) -> str:
    """Render a Go file header with optional imports immediately after `package`.

    Args:
        filename: Source JSON file name (recorded in the generated banner).
        package: Go package name.
        imports: Optional list of import paths (e.g. ['"foo/bar"', 'baz "foo/baz"']).
            When provided, an `import (...)` block is emitted right after `package`,
            which is the idiomatic location in Go.
    """
    out = [GO_HEADER_PRELUDE.format(filename=filename, package=package)]
    if imports:
        out.append("\nimport (\n")
        for imp in imports:
            out.append(f"\t{imp}\n")
        out.append(")\n")
    return "".join(out)

