def generate_go_proto_mappings() -> str:
    """Generate Go proto mapping functions from status.json."""
    data = load_json("status.json")
    
    # Filter sections with _proto_type in _meta
    sections = {}
    for k, v in data.items():
        if k.startswith("_"): continue
        meta = v.get("_meta", {})
        if "_proto_type" in meta:
            sections[k] = v
            
    if not sections:
        return ""
        
    lines = [go_header(
        "status.json",
        "mappings",
        imports=[
            '"github.com/g8e-ai/g8e/services/g8eo/internal/constants"',
            'operatorv1 "github.com/g8e-ai/g8e/services/g8eo/internal/protocol/proto/operatorv1"',
        ],
    ), "\n"]
    
    for key, values in sections.items():
        meta = values["_meta"]
        proto_type = meta["_proto_type"]
        
        # Determine internal type name
        go_type = meta.get("_go_type")
        if go_type is None:
            if key == "execution.status": go_type = "ExecutionStatus"
            else: go_type = json_key_to_go_const(key)
            
        func_name = f"ProtoTo{go_type}"
        
        lines.append(f"// {func_name} maps protobuf {proto_type} enum to internal {go_type} constants.\n")
        lines.append(f"func {func_name}(status operatorv1.{proto_type}) constants.{go_type} {{\n")
        lines.append("\tswitch status {\n")
        
        default_val = None
        for val_key, entry in values.items():
            if val_key.startswith("_"): continue
            proto_enum = entry.get("_proto_enum")
            if not proto_enum: continue
            
            const_name = entry.get("_go_const")
            if const_name is None:
                const_name = go_type + json_key_to_go_const(val_key)
                
            lines.append(f"\tcase operatorv1.{proto_type}_{proto_enum}:\n")
            lines.append(f"\t\treturn constants.{const_name}\n")
            
            if "unspecified" in proto_enum.lower() or "pending" in val_key.lower():
                default_val = f"constants.{const_name}"
                
        lines.append("\tdefault:\n")
        if default_val:
            lines.append(f"\t\treturn {default_val}\n")
        else:
            # Fallback to the first case or a generic zero value if needed
            lines.append(f"\t\treturn constants.{go_type}Pending\n")
            
        lines.append("\t}\n")
        lines.append("}\n\n")
        
    return "".join(lines)


def generate_python_proto_mappings() -> str:
    """Generate Python proto mapping dicts from status.json."""
    data = load_json("status.json")
    
    # Filter sections with _proto_type in _meta
    sections = {}
    for k, v in data.items():
        if k.startswith("_"): continue
        meta = v.get("_meta", {})
        if "_proto_type" in meta:
            sections[k] = v
            
    if not sections:
        return ""
        
    lines = [PYTHON_HEADER.format(filename="status.json"), "\n"]
    lines.append("from app.constants import ExecutionStatus\n")
    lines.append("from app.proto import operator_pb2\n\n")
    
    for key, values in sections.items():
        meta = values["_meta"]
        proto_type = meta["_proto_type"]
        
        # Determine Python class name from meta
        python_class = meta.get("_python_class")
        if python_class is None:
            print(f"Error: Status section '{key}' missing required '_python_class' field in _meta", file=sys.stderr)
            sys.exit(1)
            
        mapping_name = f"_PROTOBUF_{python_class.upper()}_TO_PYTHON"
        # Insert underscore if missing (e.g. EXECUTIONSTATUS -> EXECUTION_STATUS)
        if "STATUS" in mapping_name and "STATUS_" not in mapping_name:
            mapping_name = mapping_name.replace("STATUS", "_STATUS")
            
        lines.append(f"{mapping_name}: dict[int, {python_class}] = {{\n")
        
        for val_key, entry in values.items():
            if val_key.startswith("_"): continue
            proto_enum = entry.get("_proto_enum")
            if not proto_enum: continue
            
            python_name = entry.get("_python_name")
            if python_name is None:
                print(f"Error: Status entry '{key}.{val_key}' missing required '_python_name' field", file=sys.stderr)
                sys.exit(1)
            
            lines.append(f"    operator_pb2.{proto_enum}: {python_class}.{python_name},\n")
            
        lines.append("}\n\n")
        
        # Add helper function
        func_name = f"protobuf_{key.replace('.', '_')}_to_python"
        lines.append(f"def {func_name}(proto_val: int) -> {python_class}:\n")
        lines.append(f"    \"\"\"Convert protobuf {proto_type} to Python {python_class}.\"\"\"\n")
        lines.append(f"    if proto_val not in {mapping_name}:\n")
        lines.append(f"        raise ValueError(f\"Unknown protobuf {proto_type} value: {{proto_val}}\")\n")
        lines.append(f"    return {mapping_name}[proto_val]\n\n")
        
    return "".join(lines)


