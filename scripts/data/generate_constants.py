    data = load_json("headers.json")
    lines = [PYTHON_HEADER.format(filename="headers.json"), "\n"]
    
    # Generate constants for all headers
    for key, entry in data.items():
        if key.startswith("_"):
            continue
        
        # Require object format with metadata
        if not isinstance(entry, dict):
            print(f"Error: Entry '{key}' must be an object with 'value' and '_python_const' fields", file=sys.stderr)
            sys.exit(1)
        
        value = entry.get("value")
        if value is None:
            print(f"Error: Entry '{key}' missing required 'value' field", file=sys.stderr)
            sys.exit(1)
        
        const_name = entry.get("_python_const")
        if const_name is None:
            print(f"Error: Entry '{key}' missing required '_python_const' field", file=sys.stderr)
            sys.exit(1)
        
        lines.append(f'{const_name} = "{value}"\n')
    
    # HTTP scheme and operator-status helpers (not g8e protocol headers).
    lines.append('\nHTTP_BEARER_PREFIX = "Bearer "\n')
    lines.append('HTTP_G8E_CLIENT_HEADER = "X-G8E-Client"\n')
    lines.append('HTTP_G8E_SERVICE_HEADER = "X-G8E-Service"\n')
    lines.append('HTTP_G8E_OPERATOR_STATUS_HEADER = "X-G8E-Operator-Status"\n')

    # Generate G8eHeaders class from x-g8e.* headers
    lines.append("\nclass G8eHeaders:\n")
    lines.append('    """Collection of canonical G8E header names."""\n')
    for key, entry in data.items():
        if key.startswith("x-g8e."):
            value = entry["value"]
            # Derive attribute name: x-g8e.request-id -> REQUEST_ID
            attr_name = key.split(".")[1].replace("-", "_").upper()
            lines.append(f'    {attr_name} = "{value}"\n')

            # request-id is also exposed as EXECUTION_ID to match the
            # execution-trace terminology used by the HTTP client tracer.
            if key == "x-g8e.request-id":
                lines.append(f'    EXECUTION_ID = "{value}"\n')

    return "".join(lines)


