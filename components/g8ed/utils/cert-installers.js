/**
 * Certificate trust script generators for the HTTP onboarding server.
 *
 * Each function returns a ready-to-serve script string for the given OS.
 * All scripts:
 *   1. Escalate to root/admin if not already elevated
 *   2. Remove any existing g8e* certificates before trusting
 *   3. Download the CA certificate directly from the onboarding server
 *   4. Trust the certificate in the system store
 */

/**
 * @param {string} host - Hostname of the onboarding server (no port)
 * @returns {string} Windows batch script content
 */
export function windowsTrustScript(host, port = 80) {
    const portSuffix = port === 80 ? '' : `:${port}`;
    const url = `http://${host}${portSuffix}/ca.crt`;
    return `@echo off
:: Escalate to Administrator if not already elevated
>nul 2>&1 "%SYSTEMROOT%\\system32\\cacls.exe" "%SYSTEMROOT%\\system32\\config\\system"
if '%errorlevel%' NEQ '0' (
    echo Requesting administrative privileges...
    echo Set UAC = CreateObject^("Shell.Application"^) > "%temp%\\getadmin.vbs"
    echo UAC.ShellExecute "%~s0", "", "", "runas", 1 >> "%temp%\\getadmin.vbs"
    "%temp%\\getadmin.vbs"
    exit /B
)
if exist "%temp%\\getadmin.vbs" ( del "%temp%\\getadmin.vbs" )

echo Removing any existing g8e certificates...
for /f "tokens=2 delims==" %%s in ('certutil -store Root ^| findstr /i "g8e"') do (
    certutil -delstore Root "%%s" >nul 2>&1
)

echo Downloading g8e CA Certificate...
curl -sSf -L -o "%temp%\\g8e-ca.crt" ${url}
if %errorlevel% NEQ 0 (
    echo.
    echo ERROR: Failed to download CA certificate from ${url}
    echo Make sure the g8e platform is running and reachable.
    pause
    exit /B 1
)

echo Trusting g8e CA Certificate...
certutil -addstore -f "Root" "%temp%\\g8e-ca.crt"
if %errorlevel% NEQ 0 (
    del /f /q "%temp%\\g8e-ca.crt" >nul 2>&1
    echo.
    echo ERROR: Failed to trust the certificate. Run this script as Administrator.
    pause
    exit /B 1
)
del /f /q "%temp%\\g8e-ca.crt" >nul 2>&1
echo.
echo Certificate trusted. Restart your browser and go to https://${host}/setup
pause
`;
}

/**
 * @param {string} host - Hostname of the onboarding server (no port)
 * @returns {string} macOS bash script content
 */
export function macosTrustScript(host, port = 80) {
    const portSuffix = port === 80 ? '' : `:${port}`;
    return `#!/bin/bash
set -euo pipefail

KEYCHAIN="/Library/Keychains/System.keychain"
CERT_FILE="/tmp/g8e-ca.crt"
HOST="${host}"
URL="http://\${HOST}${portSuffix}/ca.crt"

if [ "$(id -u)" -ne 0 ]; then
    exec sudo bash "$0" "$@"
fi

echo "Removing any existing g8e certificates..."
while IFS= read -r name; do
    [ -z "$name" ] && continue
    security delete-certificate -c "$name" "$KEYCHAIN" 2>/dev/null && \\
        echo "  Removed: $name" || true
done < <(security find-certificate -a -Z "$KEYCHAIN" 2>/dev/null \\
    | awk '/^SHA-1/{sha=$NF} /"alis"/{gsub(/.*"alis"<blob>="|"$/,""); if (tolower($0) ~ /g8e/) print $0}')

echo "Downloading g8e CA Certificate..."
if ! curl -fsSL -o "$CERT_FILE" "$URL"; then
    echo ""
    echo "ERROR: Failed to download CA certificate from $URL"
    echo "Make sure the g8e platform is running and reachable."
    exit 1
fi

echo "Trusting g8e CA Certificate..."
security add-trusted-cert -d -r trustRoot -k "$KEYCHAIN" "$CERT_FILE"
rm -f "$CERT_FILE"

echo ""
echo "Certificate trusted. Restart your browser and go to https://$HOST/setup"
`;
}

/**
 * @param {string} host - Hostname of the platform
 * @param {number} httpsPort - HTTPS port (default 443)
 * @param {number} httpPort - HTTP port for CA fetch (default 80)
 * @returns {string} POSIX shell script that deploys the operator binary on any Linux system
 */
export function g8eDeploy(host, httpsPort = 443, httpPort = 80) {
    const httpPortSuffix = httpPort === 80 ? '' : `:${httpPort}`;
    const httpsPortSuffix = httpsPort === 443 ? '' : `:${httpsPort}`;
    const httpUrl = `http://${host}${httpPortSuffix}`;
    const httpsHost = `${host}${httpsPortSuffix}`;

    let portFlags = '';
    if (httpsPort !== 443) {
        portFlags = ` --wss-port ${httpsPort} --http-port ${httpsPort}`;
    }

    return `#!/bin/sh
# g8e-deploy — deploy the g8e Operator on any Linux system
# Generated by the g8e platform at ${host}
#
# Usage:
#   curl -fsSL ${httpUrl}/g8e | sh -s -- <device-link-token>
#   G8E_TOKEN=<token> curl -fsSL ${httpUrl}/g8e | sh
#
# Requirements: curl or wget, Linux (x64, arm64, or x86)
set -u

G8E_HOST="${host}"
G8E_HTTPS_HOST="${httpsHost}"
G8E_HTTP_URL="${httpUrl}"

# --- Output helpers (color only when attached to a terminal) ---
_C=0; [ -t 1 ] 2>/dev/null && _C=1
_log() { if [ \$_C -eq 1 ]; then printf '\\033[36m[g8e]\\033[0m %s\\n' "\$*"; else printf '[g8e] %s\\n' "\$*"; fi; }
_ok()  { if [ \$_C -eq 1 ]; then printf '\\033[32m[g8e]\\033[0m %s\\n' "\$*"; else printf '[g8e] %s\\n' "\$*"; fi; }
_err() { if [ \$_C -eq 1 ]; then printf '\\033[31m[g8e] %s\\033[0m\\n' "\$*" >&2; else printf '[g8e] %s\\n' "\$*" >&2; fi; }
_die() { _err "\$@"; exit 1; }

# --- Linux check ---
case "\$(uname -s)" in
  Linux) ;;
  *) _die "This script is for Linux. Detected: \$(uname -s)" ;;
esac

# --- Architecture detection ---
_arch=""
case "\$(uname -m)" in
  x86_64|amd64)        _arch=amd64 ;;
  aarch64|arm64)       _arch=arm64 ;;
  i386|i486|i586|i686) _arch=386   ;;
  *) _die "Unsupported architecture: \$(uname -m)" ;;
esac

# --- HTTP client detection (curl preferred, wget fallback) ---
_hc=0 _hw=0
command -v curl >/dev/null 2>&1 && _hc=1
command -v wget >/dev/null 2>&1 && _hw=1
[ \$_hc -eq 1 ] || [ \$_hw -eq 1 ] || _die "curl or wget is required"

_fetch() {
  if [ \$_hc -eq 1 ]; then curl -fsSL -o "\$2" "\$1"
  else wget -q -O "\$2" "\$1"; fi
}

_fetch_tls() {
  if [ \$_hc -eq 1 ]; then curl -fsSL --cacert "\$3" -H "Authorization: Bearer \$4" -o "\$2" "\$1"
  else wget -q --ca-certificate="\$3" --header="Authorization: Bearer \$4" -O "\$2" "\$1"; fi
}

# --- Token (positional arg or G8E_TOKEN env var) ---
_token="\${1:-\${G8E_TOKEN:-}}"
if [ -z "\$_token" ]; then
  if [ -t 0 ] && [ -t 1 ]; then
    printf '[g8e] Device link token: '
    read _token
  else
    _die "Token required. Usage: curl -fsSL \$G8E_HTTP_URL/g8e | sh -s -- <token>"
  fi
fi
[ -n "\$_token" ] || _die "Token cannot be empty"

# --- Temp CA cert with cleanup ---
_ca="/tmp/.g8e-ca-\$\$.pem"
_cleanup() { rm -f "\$_ca"; }
trap _cleanup EXIT INT TERM

_log "g8e Operator Deploy"
_log "  Host: \$G8E_HOST"
_log "  Arch: linux/\$_arch"
_log ""

# Step 1: Fetch platform CA certificate (plain HTTP)
_log "Fetching platform CA certificate..."
if ! _fetch "\$G8E_HTTP_URL/ca.crt" "\$_ca"; then
  _die "Failed to fetch CA certificate. Ensure the platform is running and port ${httpPort} is reachable."
fi
_ok "CA certificate ready"

# Step 2: Download operator binary (HTTPS with platform CA + bearer auth)
_log "Downloading operator binary (linux/\$_arch)..."
if ! _fetch_tls "https://\$G8E_HTTPS_HOST/operator/download/linux/\$_arch" \\
     "./g8e.operator" "\$_ca" "\$_token"; then
  _die "Download failed. Check that the token is valid and the platform is accessible on port ${httpsPort}."
fi
chmod +x ./g8e.operator
_ok "Operator binary ready (\$(wc -c < ./g8e.operator | tr -d ' ') bytes)"

# Cleanup CA cert before exec replaces this process
rm -f "\$_ca"
trap - EXIT INT TERM

# Step 3: Launch
_log "Starting operator..."
exec ./g8e.operator --device-token "\$_token" --endpoint "\$G8E_HOST"${portFlags}
`;
}

/**
 * Pipe-friendly unified trust script for macOS and Linux.
 * Detects the OS at runtime via `uname -s` and runs the appropriate
 * trust logic. Designed for: curl -fsSL http://<host>/trust | sudo sh
 *
 * @param {string} host - Hostname of the onboarding server (no port)
 * @param {number} port - HTTP port (default 80)
 * @returns {string} POSIX shell script content
 */
export function universalTrustScript(host, port = 80) {
    const portSuffix = port === 80 ? '' : `:${port}`;
    return `#!/bin/sh
set -eu

HOST="${host}"
URL="http://\${HOST}${portSuffix}/ca.crt"
CERT_FILE="/tmp/g8e-ca.crt"

if [ "\$(id -u)" -ne 0 ]; then
    echo ""
    echo "ERROR: This script must run as root."
    echo "Usage: curl -fsSL http://\${HOST}${portSuffix}/trust | sudo sh"
    echo ""
    exit 1
fi

_OS="\$(uname -s)"

echo "Downloading g8e CA certificate..."
if ! curl -fsSL -o "\$CERT_FILE" "\$URL"; then
    echo ""
    echo "ERROR: Failed to download CA certificate from \$URL"
    echo "Make sure the g8e platform is running and reachable."
    exit 1
fi

case "\$_OS" in
  Darwin)
    KEYCHAIN="/Library/Keychains/System.keychain"

    echo "Removing any existing g8e certificates..."
    security find-certificate -a -Z "\$KEYCHAIN" 2>/dev/null \\
        | awk '/^SHA-1/{sha=\$NF} /"alis"/{gsub(/.*"alis"<blob>="|"\$/, ""); if (tolower(\$0) ~ /g8e/) print \$0}' \\
        | while IFS= read -r name; do
            [ -z "\$name" ] && continue
            security delete-certificate -c "\$name" "\$KEYCHAIN" 2>/dev/null && \\
                echo "  Removed: \$name" || true
          done

    echo "Trusting g8e CA certificate..."
    security add-trusted-cert -d -r trustRoot -k "\$KEYCHAIN" "\$CERT_FILE"
    ;;

  Linux)
    echo "Removing any existing g8e certificates..."
    find /usr/local/share/ca-certificates/ -iname 'g8e*' -delete 2>/dev/null || true
    find /usr/share/ca-certificates/ -iname 'g8e*' -delete 2>/dev/null || true

    if command -v certutil >/dev/null 2>&1; then
        for db in "\$HOME/.pki/nssdb" "/etc/pki/nssdb"; do
            [ -d "\$db" ] || continue
            certutil -L -d "sql:\$db" 2>/dev/null \\
                | awk '/[Gg]8[Ee]/{print \$1}' \\
                | while IFS= read -r nick; do
                    certutil -D -d "sql:\$db" -n "\$nick" 2>/dev/null && \\
                        echo "  Removed NSS cert: \$nick" || true
                  done
        done
    fi

    echo "Trusting g8e CA certificate..."
    cp "\$CERT_FILE" /usr/local/share/ca-certificates/g8e-ca.crt
    chmod 644 /usr/local/share/ca-certificates/g8e-ca.crt
    update-ca-certificates
    ;;

  *)
    echo "Unsupported OS: \$_OS"
    rm -f "\$CERT_FILE"
    exit 1
    ;;
esac

rm -f "\$CERT_FILE"
echo ""
echo "g8e CA certificate trusted successfully."
echo "Restart your browser and navigate to https://\$HOST/setup"
`;
}

/**
 * Pipe-friendly PowerShell trust script for Windows.
 * Designed for: irm http://<host>/trust | iex
 * (run in an elevated PowerShell terminal)
 *
 * @param {string} host - Hostname of the onboarding server (no port)
 * @param {number} port - HTTP port (default 80)
 * @returns {string} PowerShell script content
 */
export function windowsPowerShellTrustScript(host, port = 80) {
    const portSuffix = port === 80 ? '' : `:${port}`;
    const url = `http://${host}${portSuffix}/ca.crt`;
    return `#Requires -RunAsAdministrator
$ErrorActionPreference = "Stop"

$url = "${url}"
$certFile = Join-Path $env:TEMP "g8e-ca.crt"

Write-Host "Removing any existing g8e certificates..."
Get-ChildItem Cert:\\LocalMachine\\Root | Where-Object { $_.Subject -like "*g8e*" -or $_.FriendlyName -like "*g8e*" } | Remove-Item -Force -ErrorAction SilentlyContinue

Write-Host "Downloading g8e CA certificate..."
try {
    Invoke-WebRequest -Uri $url -OutFile $certFile -UseBasicParsing
} catch {
    Write-Error "Failed to download CA certificate from $url. Make sure the g8e platform is running and reachable."
    exit 1
}

Write-Host "Trusting g8e CA certificate..."
certutil -addstore -f "Root" $certFile | Out-Null
if ($LASTEXITCODE -ne 0) {
    Remove-Item $certFile -Force -ErrorAction SilentlyContinue
    Write-Error "Failed to trust the certificate. Run this in an elevated PowerShell terminal."
    exit 1
}
Remove-Item $certFile -Force -ErrorAction SilentlyContinue

Write-Host ""
Write-Host "g8e CA certificate trusted successfully."
Write-Host "Restart your browser and navigate to https://${host}/setup"
`;
}

/**
 * @param {string} host - Hostname of the onboarding server (no port)
 * @param {number} port - HTTP port (default 80)
 * @returns {string} Linux bash script content
 */
export function linuxTrustScript(host, port = 80) {
    const portSuffix = port === 80 ? '' : `:${port}`;
    return `#!/bin/bash
set -euo pipefail

CERT_DEST="/usr/local/share/ca-certificates/g8e-ca.crt"
CERT_FILE="/tmp/g8e-ca.crt"
HOST="${host}"
URL="http://\${HOST}${portSuffix}/ca.crt"

if [ "$(id -u)" -ne 0 ]; then
    exec sudo bash "$0" "$@"
fi

echo "Removing any existing g8e certificates..."
find /usr/local/share/ca-certificates/ -iname 'g8e*' -delete 2>/dev/null || true
find /usr/share/ca-certificates/ -iname 'g8e*' -delete 2>/dev/null || true

if command -v certutil >/dev/null 2>&1; then
    for db in "$HOME/.pki/nssdb" "/etc/pki/nssdb"; do
        [ -d "$db" ] || continue
        certutil -L -d "sql:$db" 2>/dev/null \\
            | awk '/[Dd]rop[Oo]ps/{print $1}' \\
            | while IFS= read -r nick; do
                certutil -D -d "sql:$db" -n "$nick" 2>/dev/null && \\
                    echo "  Removed NSS cert: $nick" || true
            done
    done
fi

echo "Downloading g8e CA Certificate..."
if ! curl -fsSL -o "$CERT_FILE" "$URL"; then
    echo ""
    echo "ERROR: Failed to download CA certificate from $URL"
    echo "Make sure the g8e platform is running and reachable."
    exit 1
fi

echo "Trusting g8e CA Certificate..."
cp "$CERT_FILE" "$CERT_DEST"
chmod 644 "$CERT_DEST"
update-ca-certificates
rm -f "$CERT_FILE"

echo ""
echo "Certificate trusted. Restart your browser and go to https://$HOST/setup"
echo ""
echo "Note: Chrome/Chromium also requires a manual import:"
echo "  Settings > Privacy and security > Manage certificates > Authorities > Import"
`;
}
